public class Ex007 {
	public static void main (String[] args){
		int n1 = 30;
		int n2 = 15;
		int sub = n1 - n2;
		int mul = n1 * n2;

		System.out.println("----------------------------------------------");
		System.out.println("\t\t\t\t  RESULTS");
		System.out.println("----------------------------------------------");
		System.out.println("SUBTRACTION " + n1 + " - " + n2 + " = " + sub);
		System.out.println("MULTIPLICATION " + n1 + " * " + n2 + " = " + mul);
		System.out.println("----------------------------------------------");
	}
}